main() {
  // Operadores de atribuição (operadores binários e com posição infix)

  double a = 2;
  a = a + 3; // a += 3
  a = a - 3; // a -= 3
  a = a * 3; // a *= 3
  a = a / 5; // a /= 5
  a = a % 2; // a %= 2
  print(a);

  // Operadores relacionais (operadores binários e com posição infix. O resultado de operações relacionais é um valor bool)
  print(3 > 2); // > = "maior que";
  print(3 < 2); // < = "menor que";
  print(3 >= 2); // >= = "maior ou igual a";
  print(3 <= 2); // <= = "menor ou igual a";
  print(3 != 2); // != = "diferente";
  print(3 == 2); // == = "igual";
  print(3 == "2"); // São relacionados os tipos das variáveis analisadas;
}
